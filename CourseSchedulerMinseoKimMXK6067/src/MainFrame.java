    private void dropStudentSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentSubmitButtonActionPerformed
        String studentID = ((StudentEntry) adminDropStudentchooseStudentComboBox.getSelectedItem()).getStudentID();
        String firstName = ((StudentEntry) selectStudentDisplayComboBox.getSelectedItem()).getFirstName();
        String lastName = ((StudentEntry) selectStudentDisplayComboBox.getSelectedItem()).getLastName();
        currentSemester = currentSemesterComboBox.getItemAt(currentSemesterComboBox.getSelectedIndex());

        StudentQueries.dropStudent(studentID);
        rebuildAdminDropStudentChooseStudentComboBoxes();
        rebuildStudentComboBoxes();

        dropStudentTextArea.setText("");
        dropStudentTextArea.append(lastName + ", " + firstName + " " + studentID + " has been dropped from the list of students.\n");
        
        ArrayList<ScheduleEntry> scheduleEntries = ScheduleQueries.getScheduleByStudent(currentSemester, studentID);
        
        dropStudentTextArea.append("\nFor Semester: " + currentSemester + "\n");

        for (ScheduleEntry se: scheduleEntries) {
            ScheduleQueries.dropStudentScheduleByCourse(se.getSemester(), studentID, se.getCourseCode());
            if (se.getStatus().equals("scheduled")){
                dropStudentTextArea.append(lastName + ", " + firstName + " " + studentID + " has been dropped from " + se.getCourseCode() + "\n");
            }
            else if (se.getStatus().equals("waitlisted")) {
                dropStudentTextArea.append(lastName + ", " + firstName + " " + studentID + " has been dropped from the waitlist for " + se.getCourseCode() + "\n");

            }
            
            ArrayList<StudentEntry> studentEntry = MultiTableQueries.getWaitlistedStudentsByClass(se.getSemester(), se.getCourseCode());
            if (!studentEntry.isEmpty()) {
                StudentEntry firstWaitlistedStudent = studentEntry.get(0);
                java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
       
                ScheduleEntry studentScheduleEntry = new ScheduleEntry(se.getSemester(), se.getCourseCode(), firstWaitlistedStudent.getStudentID(), "scheduled", currentTimestamp); //then add them back
                ScheduleQueries.updateScheduleEntry(studentScheduleEntry);
        
                dropStudentTextArea.append(firstWaitlistedStudent.getLastName() + ", " + firstWaitlistedStudent.getFirstName() + " " + firstWaitlistedStudent.getStudentID() + "has been scheduled into " + se.getCourseCode() + "\n");
            }
        
        }
        
    }//GEN-LAST:event_dropStudentSubmitButtonActionPerformed

