    private void scheduleClassesSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassesSubmitButtonActionPerformed
        currentSemester = currentSemesterComboBox.getItemAt(currentSemesterComboBox.getSelectedIndex());
        String courseCode = selectClassComboBox.getItemAt(selectClassComboBox.getSelectedIndex());
        String studentName = ((StudentEntry) selectStudentComboBox.getSelectedItem()).toString();
        String studentID = ((StudentEntry) selectStudentComboBox.getSelectedItem()).getStudentID();
        
        int studentCount = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);
        int seats = ClassQueries.getClassSeats(currentSemester, courseCode);
        
        String status = "";
        if (seats == 0){
            status = "waitlisted";
        }
        else if (studentCount < seats) {
            status = "scheduled";
        }
        else {
            status = "waitlisted";
        }
        
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        
        ScheduleEntry scheduleEntry = new ScheduleEntry(currentSemester, courseCode, studentID, status, currentTimestamp);
        ScheduleQueries.addScheduleEntry(scheduleEntry);
        
        scheduleClassesStatusLabel.setText(studentName + " has been " + status + " for the class " + courseCode + ".");
    }//GEN-LAST:event_scheduleClassesSubmitButtonActionPerformed

